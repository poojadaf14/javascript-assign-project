let ABCDE = ['apple', 'ball', 'cat', 'elephant', 'dog'];

let Upperfruit = [];

for (const value of ABCDE) {
    Upperfruit.push(value. toUpperCase());
}

console.log(Upperfruit);
 
//same we can creat lowercase

let fruit = ["APPLE"," MANGO", "BANANA", "WATERMELON"];

let lowefruit = [];

for (const size of fruit) {
    lowefruit.push(size.toLowerCase());
}

console.log(lowefruit);


